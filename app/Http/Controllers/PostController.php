<?php

namespace App\Http\Controllers;

use App\Models\Post;
use App\Http\Requests\StorePostRequest;
use App\Http\Requests\UpdatePostRequest;

class PostController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $models = Post::orderBy('id', 'DESC')->get();;
        return view('posts.index', compact('models'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('posts.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StorePostRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StorePostRequest $request)
    {
        $image= $request->image;
        $imageName=time().'.'.$image->getClientOriginalExtension();
        $request->image->move('images', $imageName);
        Post::create([
            "title"=>$request->title,
            "body"=>$request->body,
           'image' =>$imageName,
        ]);

        flash('Posts Success To Create')->success();
        return redirect()->route('posts.index');
    }

    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Post  $post
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  \App\Models\Post  $post
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $model = Post::findorFail($id);
        return view('posts.edit', compact('model'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \App\Http\Requests\UpdatePostRequest  $request
     * @param  \App\Models\Post  $post
     * @return \Illuminate\Http\Response
     */
    public function update(StorePostRequest $request, $id)
    {
        $models = Post::findorFail($id);

        $image= $request->image;
        $imageName=time().'.'.$image->getClientOriginalExtension();
        $request->image->move('images', $imageName);
        $models->update([
            "title"=>$request->title,
            "body"=>$request->body,
            'image' =>$imageName,
        ]);

        flash('Post Update Success')->success();
        return redirect()->route('posts.index');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Post  $post
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        Post::destroy($id);
        flash('Post has been deleted')->success();
        return redirect()->route('posts.index');
    }
}
